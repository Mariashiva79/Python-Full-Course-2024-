# Se puede cambiar el tipo de variable con int(), float(), bool() o str()

name = "María"
age = 45
gpa = 3.2
is_student = True

# para saber el tipo de una variable:
print(type(name))
print(type(age))
print(type(gpa))
print(type(is_student))

# para obligar el tipo de una variable:
print(float(age))
print(int(gpa))
print(bool(is_student))
print(str(name))

# esto puede ser útil para sumar números cuando están en un string
